// 425p 예제 14-5 2개 반 3명 학생의 네 과목 점수를 저장하는 3차원 배열

#include <stdio.h>

int main()
{
	int s[2][3][4] = 
	{ 
		{ { 72, 80, 95, 60 }, { 68, 98, 83, 90 }, { 75, 72, 84, 90 } }, 
		{ { 66, 85, 90, 88 }, { 95, 92, 88, 95 }, { 43, 72, 56, 75 } }
	};

	for (int i = 0; i < 2; ++i) 
	{
		printf("%d반 점수...\n", (i + 1));

		for (int j = 0; j < 3; ++j) 
		{
			for (int k = 0; k < 4; ++k) 
				printf("%5d", s[i][j][k]);

			printf("\n");
		}

		printf("\n");
	}

	return 0;
}